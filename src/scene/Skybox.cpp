#include "Skybox.h"


Skybox::Skybox(const std::vector<std::string> images) : cubemap_tex(images)
{
    // create cube mesh
    float skyboxVertices[] = {
        // positions          
        -1.0f,  1.0f, -1.0f,
        -1.0f, -1.0f, -1.0f,
        1.0f, -1.0f, -1.0f,
        1.0f, -1.0f, -1.0f,
        1.0f,  1.0f, -1.0f,
        -1.0f,  1.0f, -1.0f,

        -1.0f, -1.0f,  1.0f,
        -1.0f, -1.0f, -1.0f,
        -1.0f,  1.0f, -1.0f,
        -1.0f,  1.0f, -1.0f,
        -1.0f,  1.0f,  1.0f,
        -1.0f, -1.0f,  1.0f,

        1.0f, -1.0f, -1.0f,
        1.0f, -1.0f,  1.0f,
        1.0f,  1.0f,  1.0f,
        1.0f,  1.0f,  1.0f,
        1.0f,  1.0f, -1.0f,
        1.0f, -1.0f, -1.0f,

        -1.0f, -1.0f,  1.0f,
        -1.0f,  1.0f,  1.0f,
        1.0f,  1.0f,  1.0f,
        1.0f,  1.0f,  1.0f,
        1.0f, -1.0f,  1.0f,
        -1.0f, -1.0f,  1.0f,

        -1.0f,  1.0f, -1.0f,
        1.0f,  1.0f, -1.0f,
        1.0f,  1.0f,  1.0f,
        1.0f,  1.0f,  1.0f,
        -1.0f,  1.0f,  1.0f,
        -1.0f,  1.0f, -1.0f,

        -1.0f, -1.0f, -1.0f,
        -1.0f, -1.0f,  1.0f,
        1.0f, -1.0f, -1.0f,
        1.0f, -1.0f, -1.0f,
        -1.0f, -1.0f,  1.0f,
        1.0f, -1.0f,  1.0f
    };

    std::vector<VertexP> vertices;
    for (int i = 0; i < 36; ++i) {
        vertices.push_back(VertexP(
            glm::vec3(skyboxVertices[3*i], 
                      skyboxVertices[3*i+1], 
                      skyboxVertices[3*i+2]))
        );
    }

    cube_mesh = new Mesh(vertices);
}

Skybox::~Skybox()
{
    if (cube_mesh != NULL) {
        delete cube_mesh;
        cube_mesh = NULL;
    }
}

void Skybox::bind(ShaderProgram &program, const std::string sampler_name, 
        const int index)
{
    cube_mesh->bind();
    cubemap_tex.bind(program, sampler_name, index);
}

void Skybox::unbind()
{
    cube_mesh->unbind();
    //cubemap_tex.unbind();
}
